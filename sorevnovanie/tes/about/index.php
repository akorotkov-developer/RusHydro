<p style="text-align: justify;"><font size="2">В 2019 году в г. Южно-Сахалинске пройдут Вторые корпоративные соревнования оперативного персонала ТЭС с поперечными связями Группы РусГидро. Соревнования пройдут с 5-го по 9-ое августа на базе частного образовательного учреждения дополнительного профессионального образования &laquo;Учебный центр Сахалинэнерго&raquo;. Организатор мероприятия - Филиал ПАО «РусГидро» - «Корпоративный университет гидроэнергетики».</font></p>
