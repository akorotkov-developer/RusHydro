  <br />
  </font>
<p style="text-align: justify;"><font size="2">Основными целями и задачами проведения Корпоративных соревнований являются:</font></p>
 
<ul style="text-align: justify;"> 
  <li><font size="2">повышение профессионального уровня оперативного персонала для обеспечения надежной и безопасной эксплуатации оборудования ТЭС,</font></li>
 
  <li><font size="2">обмен передовыми методами и формами организации и проведения работ по управлению оборудованием ТЭС,</font></li>
 
  <li><font size="2">снижение количества инцидентов из-за ошибочных действий оперативного персонала,</font></li>
 
  <li><font size="2">разработка и усовершенствование программно-технических средств обучения и проверки знаний персонала ТЭС.</font></li>
 </ul>
 
