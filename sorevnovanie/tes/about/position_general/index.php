<p style="text-align: center;"><strong>8. Система оценок</strong></p>
 
<ul style="text-align: justify;"> 
  <li>Оценка работ на всех этапах производится по балльной системе, на этапах №№ 1-4 с использованием применяемого на этапе программного обеспечения (машинная оценка) и с учетом требований положений о проведении этапов, а на этапах № 5 и 6 – членами судейской бригады в соответствии с положениями о проведении этапов.</li>
 
  <li>Поощрительные и штрафные баллы при выполнении заданий на этапах устанавливаются соответствующими положениями о проведении этапов соревнований.</li>
 
  <li>Протоколы по результатам выполнения заданий на этапах, подписанные старшими судьями и членами судейских бригад, передаются в секретариат Соревнований в течение часа после окончания выполнения задания.</li>
 
  <li>Протокол с результатами прохождения этапа командой утверждается главным судьей (заместителем главного судьи в его отсутствии) в течение часа после поступления в секретариат при условии неподачи апелляции руководителем команды.</li>
 </ul>
 
<br />
 
<p style="text-align: center;"><strong>9. Решение спорных вопросов</strong></p>
 
<ul style="text-align: justify;"> 
  <li>Руководитель команды имеет право знакомиться с результатами прохождения своей команды любого этапа и подавать обоснованные письменные апелляции в главную судейскую комиссию на решения судейских бригад соответствующих этапов в течение одного часа после поступления протокола в секретариат Соревнований.</li>
 
  <li>Апелляции рассматриваются главной судейской комиссией и принятые решения доводятся до заинтересованных лиц не позднее 3 часов после подачи апелляции.</li>
 
  <li>При обоснованном протесте вопрос или задание засчитываются, как выполненные. При отклонении протеста (признание его как необоснованного) участник штрафуется на количество баллов, равных максимальной оценке опротестованного вопроса или задания на этапе.</li>
 </ul>
 
<br />
 
<p style="text-align: center;"><strong>10. Подведение итогов и награждение победителей</strong></p>
 
