<p style="text-align: center;"><strong>7. Судейство соревнований</strong></p>
 
<ul style="text-align: justify;"> 
  <li>Судейство Соревнований осуществляется главной судейской комиссией и судейскими бригадами на этапах.</li>
 
  <li>Судейские бригады на этапах комплектуются из представителей ПАО «РусГидро» и других организаций, привлеченных к проведению соревнований. В исключительных случаях, к судейству привлекаются представители фирм-разработчиков программного обеспечения, используемого на этапе. Как правило, состав судейской бригады на каждом этапе – старший судья этапа и двое судей этапа. Допускается назначение судейской бригады в меньшем количестве по решению главного судьи.</li>
 
  <li>Главная судейская комиссия обеспечивает:</li>
 </ul>
 
<ul style="text-align: justify;"> 
  <li>рассмотрение и утверждение дополнительной организационной документации, необходимой для проведения судейства соревнования;</li>
 
  <li>оперативную корректировку положений о проведении отдельных этапов;</li>
 
  <li>рассмотрение и утверждение акта мандатной комиссии;</li>
 
  <li>оценку внешнего вида участников Соревнований и соответствие его требованиям настоящего Положения и положений о проведении этапов;</li>
 
  <li>инструктаж участников Соревнований по прохождению этапов Соревнований;</li>
 
  <li>разработку и корректировку графика выполнения этапов Соревнований;</li>
 
  <li>руководство и контроль за ходом Соревнований;</li>
 
  <li>принятие решений о допуске или отстранении участников;</li>
 
  <li>рассмотрение и принятие решений по апелляциям соревнующихся;</li>
 
  <li>рассмотрение и утверждение протоколов, представляемых судейскими бригадами;</li>
 
  <li>подведение итогов соревнований, утверждение итогового протокола Соревнований. 
    <ul> 
      <li>В обязанности судейских бригад на этапах входит:</li>
     </ul>
   </li>
 
  <li>разработка документации по выполнению этапа;</li>
 
  <li>ознакомление участников с рабочими местами и техническими средствами;</li>
 
  <li>организация допуска участников на рабочие места;</li>
 
  <li>выдача заданий и вводных, предусмотренных условиями решения задач;</li>
 
  <li>организация устранения неисправностей технических средств на этапах;</li>
 
  <li>контроль поведения участников и принятие решений об их снятии с этапа за нарушение условий Соревнований;</li>
 
  <li>оценка качества выполнения заданий каждой командой на этапах;</li>
 
  <li>оформление протоколов по итогам прохождения этапов.</li>
 </ul>
 
