  <li>Победителем Соревнований признается команда, набравшая наибольшее количество баллов по сумме всех этапов.</li>
 
  <li>Участники команд, набравшие наибольшую сумму баллов на всех этапах по своей специальности объявляются победителями конкурса «Лучший по профессии» в отдельных номинациях:</li>
 </ul>
 
<ul> 
  <li style="text-align: justify;">«Лучший начальник смены электростанции»;</li>
 
  <li style="text-align: justify;">«Лучший начальник смены котельного цеха»;</li>
 
  <li style="text-align: justify;">«Лучший начальник смены турбинного цеха»;</li>
 
  <li style="text-align: justify;">«Лучший начальник смены электроцеха»;</li>
 
  <li style="text-align: justify;">«Лучший начальник смены химцеха»;</li>
 
  <li style="text-align: justify;">«Лучший машинист котлов»;</li>
 
  <li>«Лучший машинист турбин». 
    <ul> 
      <li style="text-align: justify;">В случае, если две и более команд набрали одинаковое количество баллов, более высокое место присваивается команде, показавшей лучший результат на этапе № 2.</li>
     
      <li style="text-align: justify;">Итоговым документом Соревнований является протокол главной судейской комиссии (судейской комиссии), утвержденный главным судьей (заместителем главного судьи при его отсутствии).</li>
     
      <li style="text-align: justify;">Команды, занявшие в Соревнованиях первое, второе и третье места награждаются почетными дипломами, участники этих команд награждаются почетными грамотами и ценными призами. Почетными грамотами и ценными призами также награждаются победители в номинации «Лучший по профессии».</li>
     
      <li style="text-align: justify;">Командам, не занявшим призовых мест, вручаются дипломы об участии в Соревнованиях и памятные сувениры.</li>
