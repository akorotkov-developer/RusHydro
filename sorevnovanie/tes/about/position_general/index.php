<p style="text-align: center;"><strong>1. Общие положения</strong></p>
 
<ul style="text-align: justify;"> 
  <li>Вторые корпоративные соревнования оперативного персонала ТЭС с поперечными связями Группы РусГидро (далее &ndash; Соревнования) проводятся Филиалом ПАО &laquo;РусГидро&raquo; - «КорУнГ» на базе Частного образовательного учреждения дополнительного профессионального образования «Учебный центр Сахалинэнерго» (далее – ЧОУ ДПО «Учебный центр Сахалинэнерго») в городе Южно-Сахалинске Сахалинской области в период с 05.08.2019 по 09.08.2019.</li>
 
  <li>В Соревнованиях принимают участие команды оперативного персонала ТЭС с поперечными связями подконтрольных организаций Группы РусГидро (далее – ПО):</li>
 </ul>
 
<ul style="text-align: justify;"> 
  <li>АО «ДГК»;</li>
 
  <li>ПАО «Камчатскэнерго»;</li>
 
  <li>ПАО «Магаданэнерго»;</li>
 
  <li>ПАО «Сахалинэнерго»;</li>
 
  <li>АО «Чукотэнерго».</li>
 </ul>
 
<br />
 
<p style="text-align: center;"><strong>2. Основные цели и задачи соревнований</strong></p>
 
<ul style="text-align: justify;"> 
  <li>Повышение профессионального уровня оперативного персонала для обеспечения надежной и безопасной эксплуатации оборудования ТЭС.</li>
 
  <li>Обмен передовыми методами и формами организации и проведения работ по управлению оборудованием ТЭС.</li>
 
  <li>Снижение количества инцидентов из-за ошибочных действий оперативного персонала ТЭС.</li>
 
  <li>Разработка и усовершенствование программно-технических средств обучения и проверки знаний персонала ТЭС.</li>
 </ul>
 
<br />
 
<p style="text-align: center;"><strong>&nbsp;3. Требования к участию в оревнованиях</strong></p>
 
<p style="text-align: justify;">К участию в Соревнованиях допускается команда в следующем составе (указаны минимальные требования к группам по электробезопасности):</p>
 
<ul style="text-align: justify;"> 
  <li>начальник смены электростанции (V гр.);</li>
 
  <li>начальник смены котельного цеха;</li>
 
  <li>начальник смены турбинного цеха;</li>
 
  <li>начальник смены электроцеха (IV-V гр.);</li>
 
  <li>начальник смены химцеха;</li>
 
  <li>машинист котлов;</li>
 
  <li>машинист турбин.</li>
 </ul>
 
<p style="text-align: justify;">Команды участников должны формироваться из числа победителей последних проведенных соревнований профессионального мастерства оперативного персонала в ПО. Допускается включать в состав команды не более двух участников предыдущих корпоративных соревнований, при условии изменения их должности в ПО и в составе бригады.</p>
 
<ul style="text-align: justify;"> 
  <li>Каждая команда возглавляется заместителем главного инженера по эксплуатации (иное по решению главного судьи Соревнований на основании письменного обращения руководителя организации, заявляющего команду).</li>
 
  <li>Заявки на участие в Соревнованиях подаются в письменной форме в Оргкомитет до 31.03.2019 с указанием наименования ПО, Ф.И.О. участников команды (полностью), года рождения, должности, группы по электробезопасности, стажа работы в должности (согласно приложению к настоящему Положению) с копией приказа ПО об утверждении персонального состава команды.</li>
 
  <li>Участники Соревнований должны иметь навыки работы на персональном компьютере, необходимые для выполнения заданий на этапах, иметь при себе удостоверения о проверке знаний, полис обязательного медицинского страхования и другие документы в соответствии с Положением о мандатной комиссии.</li>
 
  <li>Любая используемая верхняя одежда участников команды на этапах Соревнований должна быть единого образца с наличием эмблемы и/или названия ПО.</li>
 
  <li>Замена участников Соревнований по представлению руководителя команды может быть разрешена главным судьей до начала Соревнований. Если требуется экстренная замена участника команды (болезнь или иное событие не позволяющее продолжить участие в соревнованиях), то по согласованию с главной судьей Соревнований производится замена участника на резервного (если такое возможно оперативно без нарушения графика прохождения этапов Соревнований командой и Положения о соревнованиях) или замена участника руководителем команды.</li>
 </ul>
 
<p style="text-align: justify;">&nbsp;Замена участника работником другого ПО не допускается.</p>
 
