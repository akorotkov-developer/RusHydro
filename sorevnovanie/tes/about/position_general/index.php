<p style="text-align: center;"><strong>5. Этапы соревнований</strong></p>
 
<ul style="text-align: justify;"> 
  <li>Соревнования проводятся в 6 этапов:</li>
 </ul>
 
<p style="text-align: justify;"><em>1 этап:</em><em> «Проверка знаний нормативно-технических документов (НТД)». </em></p>
 
<p style="text-align: justify;">Этап проводится с использованием специализированного программного обеспечения и разделен на 3 подэтапа:</p>
 
<ul style="text-align: justify;"> 
  <li>подэтап 1 «Знание НТД» – проверка знаний нормативно-технических документов;</li>
 
  <li>подэтап 2 «Наряд» – проверка знаний и умений применять нарядно-допускную систему для безопасной эксплуатации и ремонта оборудования;</li>
 
  <li>подэтап 3 «Видеосюжет» – проверка знаний и умений выявлять нарушения НТД при просмотре видеосюжета.</li>
 </ul>
 
<p style="text-align: justify;"><em>2 этап</em><em>: «Проверка уровня подготовки персонала котельного и турбинного цехов». </em></p>
 
<p style="text-align: justify;">Этап включает выполнение следующих заданий (подэтапов) на компьютерном тренажере ТЭС с поперечными связями:</p>
 
<ul style="text-align: justify;"> 
  <li>подэтап 1 «Пуск турбины» – проверка знаний и умений персонала при выполнении пусковых операций турбоагрегата с параллельной растопкой котлоагрегата;</li>
 
  <li>подэтап 2 «Ведение режима по диспетчерскому графику» – проверка знаний и умений персонала определить и вести экономичный режим работы технологического оборудования по диспетчерскому графику.</li>
 </ul>
 
<p style="text-align: justify;"><em>3 этап:</em><em> «Управление электротехническим оборудованием».</em></p>
 
<p style="text-align: justify;">На этапе проводится проверка профессиональных знаний и навыков у начальников смен электроцеха по управлению электротехническим оборудованием в нормальных режимах эксплуатации и при возникновении нарушений в работе оборудования ТЭС. Этап проводится на тренажере оперативных переключений в электроустановках.</p>
 
<p style="text-align: justify;"> 
  <br />
 </p>
 
<p style="text-align: justify;"><em>4 этап:</em><em> «Ведение водно-химического режима». </em></p>
 
<p style="text-align: justify;">На этапе проводится проверка теоретических знаний, практических навыков и умений начальников смен химических цехов ТЭС в части выполнения регламентных операций, анализа и поиска технологических нарушений в работе обслуживаемого оборудования.</p>
 
<p style="text-align: justify;"> 
  <br />
 </p>
 
<p style="text-align: justify;">Этап проводится с использованием специализированного программного обеспечения.</p>
 
<p style="text-align: justify;"> 
  <br />
 </p>
 
<p style="text-align: justify;"><em>5 этап: «Оказание доврачебной помощи пострадавшему».</em></p>
 
<p style="text-align: justify;">На этапе проводится проверка знаний и практических навыков оказания первой помощи пострадавшим после несчастного случая на производстве&nbsp; с использованием только подручных средств иммобилизации.</p>
 
<p style="text-align: justify;"> 
  <br />
 </p>
 
<p style="text-align: justify;"><em>6 этап: «</em><em>Ликвидация возгорания с применением средств пожаротушения». </em></p>
 
<p style="text-align: justify;">На этапе проводится проверка практических навыков оперативного персонала ТЭС по ликвидации возгораний на энергетическом оборудовании с использованием средств пожаротушения.</p>
 
<ul style="text-align: justify;"> 
  <li>Порядок и правила прохождения этапов, оценки результатов и профессиональных навыков персонала, определяются соответствующими утвержденными положениями об этапах.</li>
 
  <li>Программно-технические средства для проведения Соревнований определяются решением Оргкомитета и закрепляются в положениях о проведении этапов Соревнований.</li>
 </ul>
 
<p>&nbsp;</p>
 
<p style="text-align: center;"><strong>&nbsp;6. Порядок проведения соревнований</strong></p>
 
<ul style="text-align: justify;"> 
  <li>Для изучения нормативно-технической документации, знания которой будет проверяться в ходе Соревнований, а также для изучения порядка проведения операций на тренажере или работы со специализированным программным обеспечением, каждой команде представляется удаленный или иной доступ к тренажеру/специализированному программному обеспечению, на котором будет проводиться соответствующий этап.</li>
 </ul>
 
<p style="text-align: justify;">Доступ предоставляется командам-участницам не позднее, чем за 30 календарных дней до начала Соревнований.</p>
 
<ul style="text-align: justify;"> 
  <li>До начала Соревнований мандатная комиссия проверяет команды на соответствие требованиям настоящего Положения и Положения о мандатной комиссии. Жеребьевка стартовых номеров команд проводится в период процедуры открытия Соревнований.</li>
 
  <li>Прохождение Соревнований командами осуществляется в соответствии с утвержденным графиком движения команд по этапам и результатами жеребьевки.</li>
 
  <li>Команды допускаются к прохождению этапа при выполнении условий, установленных положением по этапу.</li>
 
  <li>Задания на выполнение работ команды получают от судей на этапах. Отсчет зачетного времени начинается после того, как судьи на этапе дают команду на выполнение задания.</li>
 
  <li>В ходе выполнения заданий этапов участники не имеют права делать замечания по действиям членов команд и судей, обсуждать свои действия с лицами, не принимающими участия в выполнении работ на этапе, или обсуждать действия судейской бригады.</li>
 
  <li>По ходу этапа и при принятии решения члены судейских бригад на этапе имеют право задавать участникам команд уточняющие вопросы.</li>
 
  <li>Руководитель команды вправе присутствовать на этапе, который проходит его команда и не имеет права вмешиваться в ход проведения этапа, делать замечания, обращаться к судьям и участникам, обсуждать действия судейской бригады.</li>
 
  <li>Участникам команд после прохождения этапов запрещается доводить задание этапа до сведения участников других команд. В случае выявления нарушения данного требования, команда по решению главной судейской комиссии может быть снята с Соревнований.</li>
 </ul>
 
